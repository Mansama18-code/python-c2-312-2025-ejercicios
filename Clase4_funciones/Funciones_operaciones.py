#Función Resta
def restar(a, b) -> int:
        
        #Funcion Resta
        """ Esta función resta dos números.
        Args:
            - parametro a: Primer número
            - parametro b: Segundo número
            - variable resultado: Resultado de la resta
        Return: 
            - La variable resultado
        """
        resultado = a - b
        return resultado

#Funcion Suma
def sumar(a, b) -> int:
        
        #Funcion Suma
        """ Esta función suma dos números.
        Args:
            - parametro a: Primer número
            - parametro b: Segundo número
            - variable resultado: Resultado de la suma
        Return: 
            - La variable resultado
        """
        resultado = a + b
        return resultado
